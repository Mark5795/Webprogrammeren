using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.ComponentModel.DataAnnotations.Schema;
using System.Data.Entity.Spatial;
using WhatsUp.Models;

namespace Whatsup.Models
{
    [Table("Users")]
    public partial class User
    {
        //set as Primary key
        [Key]
        [DatabaseGenerated(DatabaseGeneratedOption.Identity)] //switch on autogenerated
        public int Id { get; set; }

        [Required]
        public string Email { get; set; }
        public int EmailConfirmed { get; set; }
        [Required]
        public string PasswordHash { get; set; }
        public byte[] Salt { get; set; }
        public string SecurityStamp { get; set; }
        [Required]
        public string PhoneNumber { get; set; }
        public int PhoneNumberConfirmed { get; set; }
        public int TwoFactorEnabled { get; set; }
        public DateTime LockoutEndDateUtc { get; set; }
        public int LockoutEnabled { get; set; }
        public int AccessFailedCount { get; set; }
        [Required]
        public string UserName { get; set; }
        public DateTime DateCreated { get; set; }

        public User()
        {

        }

        public User(string UserName, string Email, string PhoneNumber, string PasswordHash, byte[] Salt, DateTime DateCreated)
        {
            this.UserName = UserName;
            this.Email = Email;
            this.EmailConfirmed = 0;
            this.PhoneNumber = PhoneNumber;
            this.PasswordHash = PasswordHash;
            this.Salt = Salt;
            this.SecurityStamp = "0";
            this.PhoneNumberConfirmed = 0;
            this.TwoFactorEnabled = 0;
            this.LockoutEnabled = 0;
            this.DateCreated = DateCreated;
        }

        public virtual ICollection<Contact> Contacts { get; set; }
        public virtual ICollection<Chat> Chats { get; set; }
    }
}